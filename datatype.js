// Number: Represents both integers and floating-point numbers.
let age = 25;
let price = 99.99;

// String: Represents text enclosed in single, double, or backticks.
let name = "John";
let greeting = `Hello, ${name}!`; // Template literal

// Boolean: Represents true or false.
let isActive = true;
let isCompleted = false;

// Undefined: A variable that has been declared but not assigned a value.
let result;
console.log(result); // undefined

// Null: Represents an intentional absence of any value.
let user = null;

// Object: A collection of key-value pairs. Also includes arrays, functions, and more complex structures.

let person = { name: "Alice", age: 30 };
let numbers = [1, 2, 3, 4];

// Symbol: A unique, immutable value primarily used for object property keys.
let id = Symbol("id");

// BigInt: For integers larger than Number.MAX_SAFE_INTEGER.
let bigNumber = 12345678901234567890n;
